package com.jack.util;

public class ReportList {

	private int imgOutput;//图片id
	private String showSA;//文字
	private int imgMoney;//金额图片
	private String money;//金额
	
	public int getImgOutput() {
		return imgOutput;
	}
	public void setImgOutput(int imgOutput) {
		this.imgOutput = imgOutput;
	}
	public String getShowSA() {
		return showSA;
	}
	public void setShowSA(String showSA) {
		this.showSA = showSA;
	}
	public int getImgMoney() {
		return imgMoney;
	}
	public void setImgMoney(int imgMoney) {
		this.imgMoney = imgMoney;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	
}
